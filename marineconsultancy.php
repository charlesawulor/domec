                        <h1 class="hadding-title">
                          Marine Consultancy
                        </h1>
                        <p>We have great experience in all areas of marine engineering such as new construction, conversion, and upgrades. To limit the risks you have to take as ship-owner, we can guide and supervise your special projects. We also carry out pre-purchase, damage and risk surveys on all types of workboats, tugs and offshore support vessels. Our services include, but are not limited to:</p>
						
                                  <ul class="work-process-list unstyled padding-tb-30">
                                        <li><span class="working-num ">1</span>
                                            <div class="working-text "> Cost estimation</div>
                                        </li>
                                        <li><span class="working-num">2</span>
                                            <div class="working-text">Feasibility studies</div>
                                        </li>
                                        <li><span class="working-num">3</span>
                                            <div class="working-text">Yard selection</div>
                                        </li>
                                        <li><span class="working-num">4</span>
                                            <div class="working-text">Tender support</div>
                                        </li>
                                        <li><span class="working-num">5</span>
                                            <div class="working-text">Inclining tests</div>
                                        </li>
										<li><span class="working-num">6</span>
                                            <div class="working-text">Stability books</div>
                                        </li>
										<li><span class="working-num">7</span>
                                            <div class="working-text">Drawing services</div>
                                        </li>
										<li><span class="working-num">8</span>
                                            <div class="working-text">Structural engineering</div>
                                        </li>
                                    </ul>
                        <p>The concept of ‘consultancy’ conjures up a number of differing, and sometimes contradictory images. To some within the marine industry, bad past experiences have given consultancy a poor image, but intelligent use of the correct consultant for a particular project can add value and increase the capabilities of the client organisation.</p>
						<p>For a company engaged in naval architecture and ship design, it could be argued that all activities could be described as consultancy, as separate from the construction of a vessel. Alternatively, a distinction could be drawn between newbuild design and all other projects. In our company we frequently undertake small and medium sized projects, such as updates to fire and safety plans, stability work, and engineering to enable the fitment of additional cranes or boat davits. Generally, these projects are of a routine nature, but projects of a more unusual and esoteric nature are also undertaken. These latter projects, featuring unusual problems, or requiring more advanced engineering solutions are the subject of this article</p>
						<p>For companies with an engineering capability gained through design or manufacturing it can be tempting to try to commercialise existing technical expertise by taking on consultancy work. This can be particularly attractive in an economic downturn when core business is at a low ebb. However, this is not always easy as, although the technical capability required is often similar for design as for consultancy, the way the services are organised and marketed, and the mindset required are very different.</p>
						<p>The manufacturer of a product is responsible for the delivery of the product, in its finished state to the customer. If there are any defects the manufacturer is responsible for putting them right, if there are any emergent works required as the product is being manufactured, the manufacturer needs to carry them out. This is the situation in shipbuilding, and engineers who have worked in shipbuilding tend to be very good at seeing the big picture and spotting emergent problems and fixing them.However, consultancy is different to manufacturing a product. It is not the consultant’s job to take responsibility for all of the client’s problems (although a consultant should certainly alert the client to anything outside their scope of work which they think may be problematic for the client), but to answer the question they have been asked by the client. Typically, consultancy jobs are priced competitively, such that there isn’t scope to carry out additional work whilst remaining profitable. A consultancy project must not be open-ended, but focussed.</p>
