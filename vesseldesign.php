                        <h1 class="hadding-title">
                            RESEARCH VESSEL DESIGN
                        </h1>
                        <p>General requirements for most research vessels may be similar such as packages of survey equipment, extensive accommodation, the provision of laboratories, a safe working deck, good seakeeping characteristics, low noise and the avoidance of bubble sweep-down. Other operational requirements, such as dimensional limitations, manoeuvrability, ice capability, towing requirements and speed, mean that there are many different solutions for optimising a design. The DOMEC  suite of vessels provides the basis for developing these specialised designs to meet your specific requirements.</p>
                        <p>In the design of offshore research vessels, DOMEC understands that it is vital to incorporate not only a full range of all common features, but also the individual requirements of each specialist vessel type. From polar research vessels, fishery research vessels and geotechnical research vessels to hydrographic survey vessels, oceanographic research vessels and seismic research vessels. Each vessel type has one or two features that will have an overriding and dominant influence on design. Your specialist vessels require specialist ship design.</p>
                        <p>Naturally each research vessel has basic features. These include extensive accommodation for scientists and crew, laboratory spaces for processing data, dedicated areas for sonar equipment, the means to launch and recover over-side instrumentation of all types, and large working deck areas which are easily reconfigured to provide alternative arrangements. The dedicated tasks of the research vessel you require needs special attention throughout all design stages. We listen to your requirements and incorporate these into your design. Incorporating individual requirements of each specialist vessel type in their design is vital: high towing capacities for seismic research, for example, or varying degrees of silent operation for fisheries vessels. One or two features will have an overriding and dominant influence on design in each vessel type; we make sure that your vessel will help your crew in doing their job in the most precise and secure manner possible.</p>
